mundial = int(input("Digite o número de mundiais:"))

paulista = int(input("Digite o número de títulos do campeonato paulista:"))

if(mundial == 3) and (paulista == 22) :
   print("Você é são-paulino.")
elif(mundial == 2) and (paulista == 22) :
     print("Você é santista.")
elif(mundial == 2) and (paulista == 30) :
     print("Você é corinthiano.")
else :
   print("Você é palmeirense.")
